-- ============================================================================
-- 06_repair_qualifier_tables.sql
-- Drops and recreates the 7 qualifier tables to align them with
-- 01_create_schema.sql lines 325-464.
--
-- Safe to run only when all 7 target tables are empty (the bulk load failed
-- at statement 29 with `Unknown column 'ID_CREATOR'` before any qualifier
-- rows were committed). Verify first:
--
--   SELECT 'T_WC_WIKIDATA_STATEMENT_QUALIFIER'         AS T, COUNT(*) AS N FROM T_WC_WIKIDATA_STATEMENT_QUALIFIER
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE',        COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_STRING_VALUE',      COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_STRING_VALUE
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE', COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_MEDIA_VALUE',       COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_MEDIA_VALUE
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_TIME_VALUE',        COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_TIME_VALUE
--   UNION ALL SELECT 'T_WC_WIKIDATA_QUALIFIER_QUANTITY_VALUE',    COUNT(*) FROM T_WC_WIKIDATA_QUALIFIER_QUANTITY_VALUE;
--
-- After running this script, resume the workflow:
--   python wikidata_crawler.py --start-step 110
-- ============================================================================

SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_QUANTITY_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_TIME_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_MEDIA_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_STRING_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE;
DROP TABLE IF EXISTS T_WC_WIKIDATA_STATEMENT_QUALIFIER;


CREATE TABLE T_WC_WIKIDATA_STATEMENT_QUALIFIER (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL AUTO_INCREMENT,
    ID_STATEMENT           BIGINT NOT NULL,
    ID_QUALIFIER_PROPERTY  VARCHAR(50) NOT NULL,
    QUALIFIER_HASH         VARCHAR(255) DEFAULT NULL,
    VALUE_TYPE             ENUM('item','string','external_id','media','time','quantity') NOT NULL,
    WIKIDATA_DATATYPE      VARCHAR(100) DEFAULT NULL,
    DISPLAY_ORDER          INT DEFAULT NULL,
    DELETED                TINYINT(1) DEFAULT 0,
    DAT_CREAT              DATETIME DEFAULT CURRENT_TIMESTAMP,
    TIM_UPDATED            DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    ID_CREATOR             BIGINT DEFAULT NULL,
    ID_OWNER               BIGINT DEFAULT NULL,
    ID_USER_UPDATED        BIGINT DEFAULT NULL,
    IMPORT_SOURCE          VARCHAR(100) DEFAULT NULL,
    IMPORT_BATCH_ID        VARCHAR(100) DEFAULT NULL,
    LAST_SYNC_AT           DATETIME DEFAULT NULL,
    IS_VALID               TINYINT(1) DEFAULT NULL,
    VALIDATION_ERROR       TEXT DEFAULT NULL,
    RAW_VALUE_TEXT         TEXT DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    UNIQUE KEY UK_T_WC_WIKIDATA_STATEMENT_QUALIFIER_HASH (QUALIFIER_HASH),
    KEY IDX_T_WC_WIKIDATA_STATEMENT_QUALIFIER_STATEMENT (ID_STATEMENT),
    KEY IDX_T_WC_WIKIDATA_STATEMENT_QUALIFIER_PROPERTY (ID_QUALIFIER_PROPERTY),
    KEY IDX_T_WC_WIKIDATA_STATEMENT_QUALIFIER_VALUE_TYPE (VALUE_TYPE),
    KEY IDX_T_WC_WIKIDATA_STATEMENT_QUALIFIER_IMPORT_BATCH_ID (IMPORT_BATCH_ID),
    KEY IDX_T_WC_WIKIDATA_STATEMENT_QUALIFIER_STATEMENT_PROPERTY (ID_STATEMENT, ID_QUALIFIER_PROPERTY),
    CONSTRAINT FK_T_WC_WIKIDATA_STATEMENT_QUALIFIER_STATEMENT
        FOREIGN KEY (ID_STATEMENT)
        REFERENCES T_WC_WIKIDATA_STATEMENT (ID_STATEMENT)
        ON UPDATE CASCADE
        ON DELETE CASCADE,
    CONSTRAINT FK_T_WC_WIKIDATA_STATEMENT_QUALIFIER_PROPERTY_METADATA
        FOREIGN KEY (ID_QUALIFIER_PROPERTY)
        REFERENCES T_WC_WIKIDATA_PROPERTY_METADATA (ID_PROPERTY)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL,
    ID_ITEM                VARCHAR(50) NOT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE_ID_ITEM (ID_ITEM),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE_PARENT_ITEM (ID_STATEMENT_QUALIFIER, ID_ITEM),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_ITEM_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_STRING_VALUE (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL,
    VALUE_STRING           LONGTEXT NOT NULL,
    VALUE_STRING_NORMALIZED LONGTEXT DEFAULT NULL,
    LANG_CODE              VARCHAR(20) DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_STRING_VALUE_LANG_CODE (LANG_CODE),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_STRING_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE (
    ID_STATEMENT_QUALIFIER     BIGINT NOT NULL,
    VALUE_EXTERNAL_ID          VARCHAR(1000) NOT NULL,
    VALUE_EXTERNAL_ID_NORMALIZED VARCHAR(1000) DEFAULT NULL,
    FORMATTER_URL              TEXT DEFAULT NULL,
    FORMATTER_URI_RDF          TEXT DEFAULT NULL,
    VALIDATION_STATUS          VARCHAR(100) DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE_NORMALIZED (VALUE_EXTERNAL_ID_NORMALIZED(255)),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_EXTERNAL_ID_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_MEDIA_VALUE (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL,
    FILE_NAME              VARCHAR(1000) NOT NULL,
    MEDIA_REPOSITORY       VARCHAR(100) DEFAULT NULL,
    FILE_PAGE_URL          TEXT DEFAULT NULL,
    FILE_DIRECT_URL        TEXT DEFAULT NULL,
    MIME_TYPE              VARCHAR(255) DEFAULT NULL,
    FILE_EXTENSION         VARCHAR(50) DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_MEDIA_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_TIME_VALUE (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL,
    RAW_TIME_VALUE         VARCHAR(100) DEFAULT NULL,
    TIME_PRECISION         INT DEFAULT NULL,
    CALENDAR_MODEL         VARCHAR(255) DEFAULT NULL,
    TIMEZONE_OFFSET        INT DEFAULT NULL,
    YEAR_VALUE             INT DEFAULT NULL,
    MONTH_VALUE            INT DEFAULT NULL,
    DAY_VALUE              INT DEFAULT NULL,
    DATE_START             DATE DEFAULT NULL,
    DATE_END               DATE DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_TIME_VALUE_YEAR (YEAR_VALUE),
    KEY IDX_T_WC_WIKIDATA_QUALIFIER_TIME_VALUE_DATE_START (DATE_START),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_TIME_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE T_WC_WIKIDATA_QUALIFIER_QUANTITY_VALUE (
    ID_STATEMENT_QUALIFIER BIGINT NOT NULL,
    AMOUNT                 DECIMAL(30,10) DEFAULT NULL,
    UNIT_ID_WIKIDATA       VARCHAR(50) DEFAULT NULL,
    LOWER_BOUND            DECIMAL(30,10) DEFAULT NULL,
    UPPER_BOUND            DECIMAL(30,10) DEFAULT NULL,
    AMOUNT_NORMALIZED      DECIMAL(30,10) DEFAULT NULL,
    DISPLAY_VALUE          VARCHAR(255) DEFAULT NULL,
    PRIMARY KEY (ID_STATEMENT_QUALIFIER),
    CONSTRAINT FK_T_WC_WIKIDATA_QUALIFIER_QUANTITY_VALUE_PARENT
        FOREIGN KEY (ID_STATEMENT_QUALIFIER)
        REFERENCES T_WC_WIKIDATA_STATEMENT_QUALIFIER (ID_STATEMENT_QUALIFIER)
        ON UPDATE CASCADE
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


SET FOREIGN_KEY_CHECKS = 1;
