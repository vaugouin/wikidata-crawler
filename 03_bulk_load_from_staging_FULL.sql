    s.EXPECTED_CHILD_TABLE,
    s.FORMATTER_URL,
    s.FORMATTER_URI_FOR_RDF,
    COALESCE(s.IS_SUPPORTED, 1),
    COALESCE(s.IS_ACTIVE, 1),
