INSERT INTO T_WC_WIKIDATA_STATEMENT (
    ID_STATEMENT,
    ID_WIKIDATA,
    ID_PROPERTY,
    STATEMENT_GUID,
    STATEMENT_HASH,
    VALUE_TYPE,
    WIKIDATA_DATATYPE,
    RANK,
    IS_BEST_VALUE,
    DISPLAY_ORDER,
    DELETED,
    DAT_CREAT,
    TIM_UPDATED,
    ID_CREATOR,
    ID_OWNER,
    ID_USER_UPDATED,
    IMPORT_SOURCE,
    IMPORT_BATCH_ID,
    LAST_SYNC_AT,
    IS_VALID,
    VALIDATION_ERROR,
    RAW_VALUE_TEXT
)
SELECT
    s.ID_STATEMENT,
    s.ID_WIKIDATA,
    s.ID_PROPERTY,
    s.STATEMENT_GUID,
    s.STATEMENT_HASH,
    CASE
        WHEN s.VALUE_TYPE IN ('item','string','external_id','media','time','quantity')
            THEN s.VALUE_TYPE
        ELSE NULL
    END AS VALUE_TYPE,
    s.WIKIDATA_DATATYPE,
    CASE
        WHEN s.RANK IN ('preferred','normal','deprecated') THEN s.RANK
        ELSE NULL
    END AS RANK,
    s.IS_BEST_VALUE,
    s.DISPLAY_ORDER,
    COALESCE(s.DELETED, 0),
    COALESCE(s.DAT_CREAT, NOW()),
    COALESCE(s.TIM_UPDATED, NOW()),
    s.ID_CREATOR,
    s.ID_OWNER,
    s.ID_USER_UPDATED,
    s.IMPORT_SOURCE,
    COALESCE(s.IMPORT_BATCH_ID, @IMPORT_BATCH_ID),
    s.LAST_SYNC_AT,
    s.IS_VALID,
    s.VALIDATION_ERROR,
    s.RAW_VALUE_TEXT
FROM STG_T_WC_WIKIDATA_STATEMENT s
WHERE s.IMPORT_BATCH_ID = @IMPORT_BATCH_ID
  AND s.ROW_STATUS IN ('NEW','VALID')
ON DUPLICATE KEY UPDATE
    ID_WIKIDATA       = VALUES(ID_WIKIDATA),
    ID_PROPERTY       = VALUES(ID_PROPERTY),
    STATEMENT_GUID    = VALUES(STATEMENT_GUID),
    STATEMENT_HASH    = VALUES(STATEMENT_HASH),
    VALUE_TYPE        = VALUES(VALUE_TYPE),
    WIKIDATA_DATATYPE = VALUES(WIKIDATA_DATATYPE),
    RANK              = VALUES(RANK),
    IS_BEST_VALUE     = VALUES(IS_BEST_VALUE),
