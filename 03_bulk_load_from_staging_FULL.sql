    LOCAL_VALUE_TYPE,
    EXPECTED_CHILD_TABLE,
    FORMATTER_URL,
    FORMATTER_URI_FOR_RDF,
    IS_SUPPORTED,
    IS_ACTIVE,
    LAST_SYNC_AT,
