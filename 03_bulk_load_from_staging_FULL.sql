-- ============================================================================
-- Ordered bulk-load SQL script
-- From STG_* tables into final Wikidata tables
-- With INSERT ... SELECT and ON DUPLICATE KEY UPDATE logic
--
-- Assumptions:
--   - Final tables already exist
--   - STG_* tables already exist
--   - Triggers on typed child tables are already in place
--   - Data has already been loaded into STG_* tables
--
-- Important load order:
--   1. property metadata
--   2. entity tables
--   3. statement table
--   4. typed value tables
--   5. media resolution tables
--
-- Important operational note:
--   Set @IMPORT_BATCH_ID before running.
-- ============================================================================

