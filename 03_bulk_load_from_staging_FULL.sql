    0,
    NOW()
FROM STG_T_WC_WIKIDATA_PROPERTY_METADATA s
WHERE s.IMPORT_BATCH_ID = @IMPORT_BATCH_ID
  AND s.ROW_STATUS IN ('NEW','VALID')
